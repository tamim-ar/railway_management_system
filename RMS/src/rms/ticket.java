    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        // TODO add your handling code here:
        
        String pname = passangername.getText();
        String spalce = start.getSelectedItem().toString();
        String eplace = destination.getSelectedItem().toString();
        String tno = trainnumber.getText();
        String tname = trainname.getText();
        String tprice = ttprice.getText();
        String tdate = date.getText(); 
        String ticket = numberofseat.getText();
        String ttotal = total.getText();
        
        try{
            pst = connect.prepareStatement("INSERT INTO ticket(pname,startp,destp,tnum,tname,price,date,seat,total) VALUES (?,?,?,?,?,?,?,?,?)");
            
            pst.setString(1, pname);
            pst.setString(2, spalce);
            pst.setString(3, eplace);
            pst.setString(4, tno);
            pst.setString(5, tname);
            pst.setString(6, tprice);
            pst.setString(7, tdate);
            pst.setString(8, ticket);
            pst.setString(9, ttotal);
            int k = pst.executeUpdate();
            
            if(k == 1){
                JOptionPane.showMessageDialog(this, "Ticket Booked");
                
                passangername.setText("");
                trainnumber.setText("");
                ttprice.setText("");
                total.setText("");
                numberofseat.setText("");
                trainnumber.setText("");
                total.setText("");
                trainname.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this, "Try Again");
            }
            
            //pname,startp,destp,tnum,tname,price,date,seat,total
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_confirmActionPerformed


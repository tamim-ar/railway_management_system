    public void loadstart(){
        try{
            pst = connect.prepareStatement("SELECT splace FROM addtrain");
            rs = pst.executeQuery();
            start.removeAllItems();
            
            while(rs.next())
            {
                start.addItem(rs.getString(1));
            }
        }
        catch ( Exception e){
            e.printStackTrace();
        }
        
    }
    
    public void loaddestination(){
        try{
            pst = connect.prepareStatement("SELECT desplace FROM addtrain");
            rs = pst.executeQuery();
            destination.removeAllItems();
            
            while(rs.next())
            {
                destination.addItem(rs.getString(1));
            }
        }
        catch ( Exception e){
            e.printStackTrace();
        }
        
    }
    
    public void load(){
        String startp = start.getSelectedItem().toString();
        String destinationp = destination.getSelectedItem().toString();
        
        try{
            pst= connect.prepareStatement("SELECT * FROM addtrain WHERE splace =? and desplace = ?");
            pst.setString(1, startp);
            pst.setString(2, destinationp);
            rs = pst.executeQuery();
            
            if(rs.next() == true){
                trainname.setText(rs.getString(2));
                trainnumber.setText(rs.getString(1));
                ttprice.setText(rs.getString(5));
            }
            else{
                JOptionPane.showMessageDialog(this, "Not Available");
            }
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }

